class FasterRCNNBase(nn.Module):
    """
    Main class for Generalized R-CNN.

    Arguments:
        backbone (nn.Module):
        rpn (nn.Module):
        roi_heads (nn.Module): takes the features + the proposals from the RPN and computes
            detections / masks from it.
        transform (nn.Module): performs the data transformation from the inputs to feed into
            the model
    """

    def __init__(self, backbone, rpn, roi_heads, transform):
        super(FasterRCNNBase, self).__init__()
        self.transform = transform
        self.backbone = backbone
        self.rpn = rpn
        self.roi_heads = roi_heads
        self.cbam = CBAM(backbone.out_channels)
        # self.cbam_after_rpn = CBAM(backbone.out_channels)
        # self.cbam_after_roi = CBAM(backbone.out_channels)
           
        # used only on torchscript mode
        self._has_warned = False
        
        # self.ca = CoordAtt(backbone.out_channels, backbone.out_channels)
        

    @torch.jit.unused
    def eager_outputs(self, losses, detections):
        # type: (Dict[str, Tensor], List[Dict[str, Tensor]]) -> Union[Dict[str, Tensor], List[Dict[str, Tensor]]]
        if self.training:
            return losses

        return detections

    def forward(self, images, targets=None):
        # type: (List[Tensor], Optional[List[Dict[str, Tensor]]]) -> Tuple[Dict[str, Tensor], List[Dict[str, Tensor]]]
        """
        Arguments:
            images (list[Tensor]): images to be processed
            targets (list[Dict[Tensor]]): ground-truth boxes present in the image (optional)

        Returns:
            result (list[BoxList] or dict[Tensor]): the output from the model.
                During training, it returns a dict[Tensor] which contains the losses.
                During testing, it returns list[BoxList] contains additional fields
                like `scores`, `labels` and `mask` (for Mask R-CNN models).

        """
        if self.training and targets is None:
            raise ValueError("In training mode, targets should be passed")

        if self.training:
            assert targets is not None
            for target in targets:         # Further determine if the boxes parameter of the incoming target is compliant or not
                boxes = target["boxes"]
                if isinstance(boxes, torch.Tensor):
                    if len(boxes.shape) != 2 or boxes.shape[-1] != 4:
                        raise ValueError("Expected target boxes to be a tensor"
                                         "of shape [N, 4], got {:}.".format(
                                          boxes.shape))
                else:
                    raise ValueError("Expected target boxes to be of type "
                                     "Tensor, got {:}.".format(type(boxes)))

        original_image_sizes = torch.jit.annotate(List[Tuple[int, int]], [])
        for img in images:
            val = img.shape[-2:]
            assert len(val) == 2  # prevent nonsense size
            original_image_sizes.append((val[0], val[1]))
        # original_image_sizes = [img.shape[-2:] for img in images]

        images, targets = self.transform(images, targets)  # preprocess images and targets
        # print(images.tensors.shape)
        features = self.backbone(images.tensors)  # forward backbone network and get features
        
        if isinstance(features, torch.Tensor):  # if only one feature level is used, convert it to an iterable
            features = OrderedDict([('0', features)])  # If the prediction is made on a multilayer feature layer, the incoming is an ordered dictionary
            
        # Apply CBAM on each feature level
        for key, feature in features.items():
            features[key] = self.cbam(feature)
        
        # # Apply CA on each feature level
        # for key, feature in features.items():
        #     features[key] = self.ca(feature)
        
        # forward RPN network and get proposals
        # proposals: List[Tensor], Tensor_shape: [num_proposals, 4],
        # each proposal is [x1, y1, x2, y2] in the image coordinate space
        proposals, proposal_losses = self.rpn(images, features, targets)
        
#         # Apply CBAM on each feature level
#         for key, feature in features.items():
#             features[key] = self.cbam_after_rpn(feature)
        
        # # Apply CA on each feature level
        # for key, feature in features.items():
        #     features[key] = self.ca(feature)

        # forward data generated by the rpn and the labeled target information into the second half of the fast rcnn
        detections, detector_losses = self.roi_heads(features, proposals, images.image_sizes, targets)
        # Post-processing of the network's predictions (mainly reducing bboxes to the original image scale)
        detections = self.transform.postprocess(detections, images.image_sizes, original_image_sizes)

        losses = {}
        losses.update(detector_losses)
        losses.update(proposal_losses)

        if torch.jit.is_scripting():
            if not self._has_warned:
                warnings.warn("RCNN always returns a (Losses, Detections) tuple in scripting")
                self._has_warned = True
            return losses, detections
        else:
            return self.eager_outputs(losses, detections)

        # if self.training:
